export function updateServerTable(servers) {
    const tableBody = document.querySelector('.server-table tbody');
    if (!tableBody) return;

    tableBody.innerHTML = '';
    servers.forEach(server => {
        const row = createServerRow(server);
        tableBody.appendChild(row);
    });
}

